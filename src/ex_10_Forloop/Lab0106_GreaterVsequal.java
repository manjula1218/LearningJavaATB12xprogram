package ex_10_Forloop;

public class Lab0106_GreaterVsequal {
    public static void main(String[] args) {
        for (int i = 0; i <=10 ; i++) {
            System.out.println(i);//0 to 10 but 11 times

        }
        for (int i = 0; i <10 ; i++) {
            System.out.println(i);//0 to 9 but 10 times

        }
        for (int i = 1; i <=10 ; i++) {
            System.out.println(i);//1 to 10 but 10 times

        }
    }
}
