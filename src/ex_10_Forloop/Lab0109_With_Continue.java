package ex_10_Forloop;

public class Lab0109_With_Continue {
    public static void main(String[] args) {
        for(int i=0;i<=11;i++)
        {
            //System.out.println(i);
            if(i==10)
            {
                continue;
                //System.out.println(i);
            }
            System.out.println(i);
        }
    }
}
